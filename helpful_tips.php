				<h2>Helpful Tips</h2>
				<p>Many people every year are stopped and arrested for OUI/DUI. Below are some helpful tips in case you are ever in the situation of being stopped by the police and suspected for an OUI.</p>
				<ul class="content_list">
					<li>Know where your license and registration is at all times. In many OUI arrest reports the police will say that the defendant "was bumbling around looking for his or her license or registration." The police will say this to try to paint a picture of an intoxicated person digging through papers looking for something that they should have readily available. By knowing exactly where these things are you can avoid the police saying this.</li>
					<li>Be polite and courteous to the police, do not make an already tense situation worse.</li>
					<li>You are required to identify yourself to police, however say as little as possible. In many OUI police reports the police will say that the defendant had "slurred speech or spoke in a thick tongue." The police say this in order to attempt to prove that the defendant was intoxicated, the less words you say the less opportunity there is for the police to say you have slurred speech.</li>
					<li>Do not say anything that may incriminate you. In Massachusetts you have a right to be free from self incrimination, therefore you do not have to answer any questions that the police may use against you as evidence. Therefore when the police officer asks "Have you been drinking tonight" or "how many drinks have you had tonight", do not answer the question. Any admission of drinking alcohol will be used against you at trial to prove alcohol intake.</li>
					<li>A person can refuse the breathalyzer. Every situation is different regarding whether a person should or should not take the breathalyzer, however since it is impossible to know your actual BAC before you blow it is usually the best course of action to refuse to take this test. A failed breathalyzer will be a strong piece of evidence against you in court. The RMV will take your license based on the breathalyzer refusal but this refusal cannot be introduced at trial.</li>
					<li>A person can refuse to take the field sobriety tests. The results of field sobriety tests are usually used against a you at trial. By refusing these tests you are depriving the prosecution of evidence that can be used against you at trial and unlike a refused breathalyzer the RMV will not suspend your license for refusing the field sobriety tests. The prosecution also cannot mention that you refused the field sobriety tests at trial.</li>
					<li>If you are arrested for OUI/DUI you have the right to a comparison blood test to test your blood alcohol content within a reasonable amount of time and at your own cost.</li>
					<li>As mentioned above you have the right to remain silent, besides identifying yourself, you do not have to answer any questions the police ask you.</li>
					<li>If arrested you have the right to make a phone call to communicate with others to arrange bail or obtain the services of an attorney.</li>
					<li>You have the right to an Attorney to represent you in any OUI/DUI case and be present for any questioning by police.</li>
				</ul>
