				<h2>Field Sobriety Test</h2>
				<p>Field Sobriety Tests are roadside tests the police officers use to determine if a person is intoxicated. If the police officer determines that a person fails the test or tests then this is used as evidence against that person in court. It is important to understand that many times these tests are done at night, when a police officer is shining a flashlight the person's face, most likely the person taking the test is nervous and these tests are administered on the side of a road. <span class="excerpt">It is important to understand that a person has a right to refuse to take any field sobriety tests and the prosecution cannot mention the refusal to take the tests at trial.</span> These are not ideal conditions to take a test which may determine if someone goes home or gets arrested. These field sobriety tests are not 100% accurate and there are problems with each test. All of these tests require a certain level of physical health and as mentioned above are usually administered in less then ideal conditions. It is important to understand that a person has a right to refuse to take any field sobriety tests and the prosecution cannot mention the refusal to take the tests at trial. Unlike the breathalyzer test there is no RMV suspension for refusing to take the field sobriety tests so it is usually the best course of action to refuse to take the tests. The National Highway Traffic and Safety Administration has validated three field sobriety tests and a description of these test are below. Please contact Attorney Cyzeski for a free consultation regarding your case.</p>
				<h3>Horizontal Gaze Nystagmus Test (HGN)</h3>
				<p>This test requires a person to stare at an object, usually the lighted pen of a police officer. The object is then moved back and forth while the police officer looks to see if the eye is jerking (nystagmus) instead of moving in a smooth pursuit of the object. Alcohol can cause this jerking of the eye, however many other factors can cause this jerking like a physical condition such as multiple sclerosis or certain medications. In order for this test to be admissible at trial the prosecution must prove that it was reliable and the person administering the test was qualified to do so. In most cases this test cannot be introduced at trial.</p>
				<h3>Nine Step walk and Turn</h3>
				<p>This test requires a person to take nine steps, heel to toe. Once the person has taken these nine steps they have to turn around and walk back heel to toe all while staying in a straight line. Many other factors besides intoxication can cause a person not to be able to perform this test such as physical problems, obesity, old age, type of footwear or lack of footwear, weather conditions or the condition of the road.</p>
				<h3>One Leg Stand</h3>
				<p>This test requires a person to stand on one leg for about 30 seconds while keeping balance and counting out loud. If there is any hopping, wobbling or raising the arms for balance the police officer will hold this against the person taking the test. Many other factors besides intoxication can cause a person not to be able to perform this test such as physical problems, obesity, old age, type of footwear or lack of footwear, weather conditions or the condition of the road.</p>
